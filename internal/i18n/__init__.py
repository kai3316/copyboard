class LocaleManager:
    """Manages the active locale and provides translation lookup."""

    def __init__(self, active: str = DEFAULT_LOCALE) -> None:
        self._active = active
        logger.debug("LocaleManager initialised: active=%s", active)

    @property
    def active(self) -> str:
        return self._active

    @active.setter
    def active(self, locale: str) -> None:
        # Normalize case: "zh-cn" → "zh-CN", "en-us" → "en-US"
        parts = locale.split("-", 1)
        if len(parts) == 2:
            normalized = f"{parts[0].lower()}-{parts[1].upper()}"
        else:
            normalized = locale.lower()
        if normalized not in LOCALES and locale not in LOCALES:
            logger.warning("Unknown locale '%s', falling back to '%s'", locale, DEFAULT_LOCALE)
            normalized = DEFAULT_LOCALE
        self._active = normalized if normalized in LOCALES else locale

    @property
    def available(self) -> list[str]:
        return list(LOCALES.keys())

    def t(self, key: str, **fmt: Any) -> str:
        """Look up a translation key in the active locale.

        Falls back to the default locale, then to the raw key.
        Supports ``{name}`` format placeholders.
        """
        table = LOCALES.get(self._active, _EN)
        text = table.get(key)
        if text is None:
            text = _EN.get(key, key)
        if fmt:
            try:
                text = text.format(**fmt)
            except (KeyError, ValueError):
                logger.debug("i18n format failed for key=%r, fmt=%r", key, fmt)
        return text


# Module-level singleton for convenience
_locale_mgr = LocaleManager()


def set_locale(locale: str) -> None:
    """Set the active language (e.g. 'en', 'zh-CN')."""
    _locale_mgr.active = locale


def get_locale() -> str:
    """Return the currently active locale code."""
    return _locale_mgr.active


def available_locales() -> list[str]:
    """Return list of available locale codes."""
    return _locale_mgr.available


def T(key: str, **fmt: Any) -> str:
    """Translate a key into the current locale. Usage: T('key.name', name='foo')."""
    return _locale_mgr.t(key, **fmt)
